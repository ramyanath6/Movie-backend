import mongoose from "mongoose";

const movieSchema=new mongoose.Schema({
    name:{
        type:String,
        required:true
    },
    description:{
        type:String,
        required:true
    },
    price:{
        type:Number,
        required:true
    },
    image:{
        type:String,
        required:true
    },
    category:{
        type:String,
        required:true
    }
});

const movieModel=mongoose.model('moviecollection',movieSchema);
export default movieModel;